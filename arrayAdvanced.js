//array declareration 
var friendsAge = [21, 20, 23, 24, 25, 22];
console.log(friendsAge);

// add element in the last
friendsAge.push(23);
console.log(friendsAge);

//remove element in the last
friendsAge.pop();
console.log(friendsAge);

// remove element in the first
friendsAge.shift();
console.log(friendsAge);

//add fist eleemnt in a array
friendsAge.unshift(34);
console.log(friendsAge);

//array length
console.log(friendsAge.length);